#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*dest;

	dest = (char*)s;
	if (s)
	{
		while (*dest != c)
		{
			if (*dest == '\0')
				return (NULL);
			++dest;
		}
	}
	return (dest);
}
